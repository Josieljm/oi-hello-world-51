      const utterance = new SpeechSynthesisUtterance();
      
      // ‚úÖ CONFIGURA√á√ïES PARA VOZ CARISM√ÅTICA E ENVOLVENTE COM EMO√á√ÉO
      const voiceSettings = getVoiceSettings(emotion);
